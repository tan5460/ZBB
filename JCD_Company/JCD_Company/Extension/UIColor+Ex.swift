    /// 根据hex 获取颜色
    ///
    /// - Parameters:
    ///   - hex: hex
    ///   - alpha: alpha
    /// - Returns: color
    static func hexColor(_ hexString: String, alpha: CGFloat = 1) -> UIColor {
        let hexString = hexString.trimmingCharacters(in: .whitespacesAndNewlines)
        let scanner = Scanner(string: hexString)
        
        if hexString.hasPrefix("#") {
            scanner.scanLocation = 1
        }
        
        var color: UInt32 = 0
        scanner.scanHexInt32(&color)
        
        let mask = 0x000000FF
        let rft = Int(color >> 16) & mask
        let gft = Int(color >> 8) & mask
        let bft = Int(color) & mask
        
        let red   = CGFloat(rft) / 255.0
        let green = CGFloat(gft) / 255.0
        let blue  = CGFloat(bft) / 255.0
        return UIColor(red: red, green: green, blue: blue, alpha: alpha)
    }
    
    convenience init(red1: CGFloat, green1: CGFloat, blue1: CGFloat, alpha: CGFloat = 1.0) {
